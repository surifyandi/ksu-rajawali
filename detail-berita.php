				<article class="col-md-9 left-bar detail-news">
					<h3>Pemerintah Bakal Bekukan 61 Ribu Koperasi Seluruh Indonesia</h3>
					<div class="meta">
						<span class="date">21 September 2016</span>
						<span class="author">Admin 1</span>
					</div>
					<img src="images/img-detail-berita.jpg">
					<p>
						Sebanyak 61 ribu koperasi di seluruh Indonesia terancam dibekukan. Dari jumlah tersebut, sekitar 9.968 koperasi berada di wilayah Jawa Barat. Sekretaris Kementerian Koperasi dan Usaha Kecil Menengah (UKM), Agus Muharram, mengatakan hal menyusul implementasi program reformasi koperasi yang dibesut oleh pihaknya. Saat ini payung hukum dalam bentuk Ketetapan Menteri (Kepmen) sedang disusun dan diproyeksikan selesai bulan ini. Dengan demikian, mulai bulan depan koperasi non-aktif sudah mulai dibekukan.
					</p>
					<p>
						"Teknisnya, badan hukum koperasi yang terancam dibekukan akan diumumkan melalui Dinas Koperasi. Mereka diberi waktu tiga bulan. Jika tidak ada respons, mereka secara resmi akan dibekukan," ujar dia dalam keterangan tertulis di Jakarta, Kamis (11/8/2016). Menurut dia, pembekuan koperasi nonaktif tersebut merupakan bagian dari reformasi total koperasi, dengan melakukan reorientasi dari kuantitas ke kualitas. Target Kementerian Koperasi, ke depan tidak ada lagi koperasi yang sekadar papan nama.
					</p>
					<p>
						"Yang terpenting adalah jumlah anggota koperasi yang sejalan dengan nilai transaksi mereka dalam koperasi masing-masing, bukan jumlah koperasi. Umumnya, koperasi yang nonaktif terkendala masalah SDM, mulai dari attitude hingga rendahnya kemampuan manajerial mereka," kata dia. Agus mengungkapkan, kebijakan reformasi koperasi harus dilakukan agar koperasi yang ada menjadi mandiri, sehat, profesional dan mampu bersaing hingga ke kancah global. Reformasi dilakukan di semua aspek, mulai dari pembenahan kelembagaan hingga sumber daya manusia (SDM) koperasi.  "Ke depan koperasi di Indonesia harus tumbuh menjadi besar modern dan mandiri dengan kekuatan anggota, bukan mengandalkan bantuan pemerintah. Tidak ada lagi koperasi tanpa TI (teknologi informasi), tidak ada koperasi tanpa transaksi, dan tidak ada koperasi tanpa pelatihan," ucap dia. Guna menunjang reformasi tersebut, tutur Agus, pemerintah juga akan melakukan revisi Undang-undang (UU) Koperasi. Saat ini draft revisi UU tersebut sudah masuk ke Dewan Perwakilan Rakyat (DPR).
					</p>
					<p>
						Di sisi lain, Agus berharap pemerintah daerah menyederhanakan sejumlah peraturan daerah (perda) untuk mengembangkan iklim usaha kondusif bagi perkoperasian. Dengan demikian, target tumbuhnya koperasi besar berskala internasional di Indonesia akan tercapai. Sementara itu, Wakil Gubernur Jabar Deddy Mizwar mengatakan ia mendukung penuh upaya reformasi koperasi yang diusung Kementerian Koperasi dan UKM. Menurut dia, secara lembaga, koperasi harus dikelola dengan manajemen profesional seperti layaknya perusahaan. "Koperasi harus dikelola secara modern agar bisa tumbuh besar dan go internasional. Koperasi jangan hanya memberikan harapan kosong, banyak nama, tapi kualitas dipertanyakan," kata dia.
					</p>
					<p>
						Deddy berharap ke depan akan muncul koperasi-koperasi besar dari Jawa Barat yang berorientasi bisnis global. Saat ini, sudah ada bibit-bibit koperasi besar tersebut, walaupun di sisi lain banyak juga koperasi yang tinggal papan nama. Berdasarkan data Dinas Koperasi dan Usaha Mikro Kecil Menengah (KUMKM) Jabar, dari 25.646 di Jabar, 15.678 di antaranya aktif dan 9.968 koperasi tidak aktif. Secara nasional, jumlah koperasi yang aktif berjumlah 81 ribu-82 ribu dari total sekitar 212 ribu koperasi. 
					</p>
